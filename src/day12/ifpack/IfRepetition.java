package day12.ifpack;

public class IfRepetition {
    public static void main(String[] args) {
        int s1=4;
        int s2=8;

        if (s1>s2){
            System.out.println("S1 is bigger.");
        }else if (s1==s2) {
            System.out.println("S1 and S2 are same.");
        }else {
            System.out.println("S1 is smaller.");
        }
    }
}
